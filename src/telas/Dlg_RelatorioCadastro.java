    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        if (!comboCadastro.getSelectedItem().toString().equals("Selecione")) {

            try {
                // Compila o relatorio
                JasperReport relatorio = JasperCompileManager.compileReport(getClass().getResourceAsStream("/SmartSales/src/Relatorios/Cadasros"));

                // Mapeia campos de parametros para o relatorio, mesmo que nao existam
                Map parametros = new HashMap();
                parametros.put("cadastro", comboCadastro.getSelectedItem().toString());

                // Executa relatoio
                JasperPrint impressao = JasperFillManager.fillReport(relatorio, parametros, ConexaoBD.getInstance().getConnection());

                // Exibe resultado em video
                JasperViewer.viewReport(impressao, false);

                //dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao gerar relat칩rio: " + e);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Cadastro Valido");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

